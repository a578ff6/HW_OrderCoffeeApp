import UIKit

/// 顏色風格的擴展
extension UIColor {
    
    /// 星巴克主體色
    static let deepGreen = UIColor(red: 0, green: 112/255, blue: 74/255, alpha: 1)
    
    /// 深咖啡
    static let deepBrown = UIColor(red: 39/255, green: 37/255, blue: 31/255, alpha: 1)
    
    /// 非常淡的灰色，接近白色
    static let lightWhiteGray = UIColor(red: 0.9, green: 0.9, blue: 0.9, alpha: 1.0)
    
}
